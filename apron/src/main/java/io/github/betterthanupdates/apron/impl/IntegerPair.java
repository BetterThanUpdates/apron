package io.github.betterthanupdates.apron.impl;

public class IntegerPair {
	private final int first;
	private final int second;

	public IntegerPair(int first, int second) {
		this.first = first;
		this.second = second;
	}

	public int first() {
		return first;
	}

	public int second() {
		return second;
	}
}
